class Solution{
  public:
    int countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        int ws = 0, we = 0;
        long long p = 1;
        int ans = 0;
        while(we < n) {
            p *= a[we];
            we++;
            while(p >= k && ws < we) {
                p /= a[ws];
                ws++;
            }
            ans += (we - ws);
        }
        return ans;
    }
};

